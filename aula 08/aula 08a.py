import math

# n1 = int(input('Enter a number '))
n1 = 29
root = math.sqrt(n1)
print('The root of number {} is {}'.format(n1, root))
print('The root of number {} is {}'.format(n1, math.floor(root)))
print('The root of number {} is {}'.format(n1, math.ceil(root)))
print('The root of number {} is {:.2f}'.format(n1, root))
